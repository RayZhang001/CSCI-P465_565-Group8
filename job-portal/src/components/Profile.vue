    <div class="my-5 container px-0 shadow-sm mb-5 bg-white rounded" v-if="role === 'student'">
      <b-card no-body>
        <b-tabs card style="font-size: 17px;">
          <b-tab title="Acceptances" active style="max-height: 1000px; overflow-y: auto;">
            <b-card-body>
              <span v-if="user.acceptances.length === 0">You don't have any acceptances! Start applying!</span>
              <div v-for="(job, idx) in user.acceptances" class="text-left mt-2">
                <b-card :title="job.title">
                  <button href="#" style="float: right; margin-top: -37px !important;" class="mt-3 pt-2 ml-2 btn btn-outline-danger" @click="rejectConfirmedApplicant(job._id, user._id)"><i class="ti-close"></i></button>
                  <button href="#" style="float: right; margin-top: -37px !important;" class="mt-3 pt-2 btn btn-outline-info"><i class="ti-comment-alt"></i></button>
                  <b>Company:</b><p>{{job.company}}</p>
                  <b>Recruiter: </b><p>{{job.employer.name}}</p>
                  <b>Position:</b><p>{{job.position}}</p>
                  <b>Description:</b><p>{{job.description}}</p>
                </b-card>
              </div>
            </b-card-body>
          </b-tab>
          <b-tab title="Education" style="max-height: 1000px; overflow-y: auto;">
            <b-card-body style="font-size: 22px;">
              <div v-if="user.education.length > 0" v-for="edu in user.education" :id="edu.school">
                <b-card class="mb-3">
                  <button style="float: right; border: none;" class="btn btn-outline-danger ml-2" @click="deleteEducation(edu)"><i class="ti-close"></i></button>
                  <button style="float: right; border: none;" class="btn btn-outline-secondary" @click="editEducationModal(edu)"><i class="ti-pencil"></i></button>
                  <div><b>Name</b>
                    <p>{{edu.school}} </p></div>
                  <div><b>Degree</b>
                    <p>{{edu.degree}} </p></div>
                  <div><b>Field of Study</b>
                    <p>{{edu.fieldofstudy}} </p></div>
                  <div><b>Duration</b>
                    <p>{{$moment(edu.from).format('MMM Do YY') }} - {{edu.to !== null ? $moment(edu.to).format('MMM Do YY') : 'Present'}} </p></div>
                </b-card>
              </div>
              <b-button
                v-if="role === 'student'"
                style="width: 100%; border-radius: 10px;"
                variant="outline-warning"
                class="mb-2 mt-1 "
                @click="addEducationModal"
              >
                Add
              </b-button>
            </b-card-body>
          </b-tab>
          <b-tab title="Experiences" style="max-height: 1000px; overflow-y: auto;">
            <b-card-body style="font-size: 22px;">
              <div v-if="user.experience.length > 0" v-for="exp in user.experience">
                <b-card class="mb-3">
                  <button style="float: right; border: none;" class="btn btn-outline-danger ml-2" @click="deleteExperience(exp)"><i class="ti-close"></i></button>
                  <button style="float: right; border: none;" class="btn btn-outline-secondary" @click="editExperienceModal(exp)"><i class="ti-pencil"></i></button>
                  <div><b>Company/Organization</b>
                    <p>{{exp.company}} </p></div>
                  <div><b>Title</b>
                    <p>{{exp.title}} </p></div>
                  <div><b>Location</b>
                    <p>{{exp.location}} </p></div>
                  <div><b>Duration</b>
                    <p>{{$moment(exp.from).format('MMM Do YY') }} - {{formatDate(exp.to)}} </p>
                  </div>
                  <div><b>Description</b>
                    <p>{{exp.description}}</p></div>
                </b-card>
              </div>
              <b-button
                v-if="role === 'student'"
                style="width: 100%; border-radius: 10px;"
                variant="outline-warning"
                class="mb-2 mt-1 "
                @click="addExperienceModal"
              >
                Add
              </b-button>
            </b-card-body>
          </b-tab>
          <b-tab title="Skills" style="max-height: 1000px; overflow-y: auto; min-height: 400px;">
            <b-card-body style="font-size: 22px;">
              <b-form class="text-left">
                <label>Add/Remove Skills:</label>
                <b-form-group>
                  <SkillSelect @addSkills="addSkills" :recievedValues="user.skills"/>
                  <button class="btn btn-outline-warning" @click.stop="updateSkills" style="width: 100%; border-radius: 10px;">Save</button>
                </b-form-group>
              </b-form>
              <hr />
            </b-card-body>
          </b-tab>
        </b-tabs>
      </b-card>
    </div>

    <div class="my-5 container px-0 shadow-sm mb-5 bg-white rounded" v-if="role === 'employer'">
      <b-card no-body>
        <b-tabs card style="font-size: 18px;">
          <b-tab title="Job Posting" active style="max-height: 1000px; overflow-y: auto;">
            <b-card-body>
              <b-input-group class="mb-3">
                <b-form-input placeholder="Search for job" v-model="employerSearchJob"></b-form-input>
                <b-input-group-append>
                  <b-button variant="warning" style="border: 1px solid #dba30e"><i class="ti-search"></i></b-button>
                </b-input-group-append>
              </b-input-group>
              <div v-for="(job, idx) in employerJobs">
                <b-card class="text-left my-2">
                  <button href="#" style="float: right; margin-top: 5px !important; border: none;"
                          class="mt-3 pt-2 ml-2 btn btn-outline-danger" @click="deleteConfirmModal(job)"><i
                    class="ti-close"></i></button>
                  <button href="#" style="float: right; margin-top: 5px !important; border: none;"
                          class="mt-3 pt-2 btn btn-outline-info" @click="jobInfoModal(job)"><i class="ti-pencil"></i>
                  </button>
                  <div class="row">
                    <div style="" class="col-lg-2 col-mg-2 col-sm-12 pr-0">
                      <img style="height: 100px; width: 100px; object-fit: cover;" src="../assets/company.jpg">
                    </div>
                    <div style="text-align: justify" class="col-lg-10 col-md-8 col-sm-12 px-0">
                      <h3 class="card-title">{{job.title}}</h3>
                      <span class="mt-5" style="margin-right: 4px"><i class="ti-location-pin"></i></span>
                      <span>{{job.location}}</span>
                      <p></p>
                      <span style="font-weight: bold; margin-right: 5px"><i class="ti-briefcase"></i></span>
                      <span>{{job.position}}</span>
                      <p></p>
                      <div class="row">
                        <div class="col-lg-1 pr-0" style="width: 10px !important;">
                          <span style="font-weight: bold;"><i class="ti-receipt"></i></span>
                        </div>
                        <div class="col-lg-11 px-0" style="margin-left: -22px !important;">
                          <span style="white-space: pre-wrap;">{{job.description}}</span>
                        </div>
                      </div>
                      <p></p>
                      <span style="font-weight: bold; margin-right: 9px"><i class="ti-star"></i></span>
                      <span>{{job.skills.length > 0 ? job.skills.map(s => s.name).join(', ') : 'None'}}</span>
                      <p></p>
                      <span style="font-weight: 400; margin-right: 9px !important;"><i class="ti-stats-up"></i></span>
                      <span>{{job.applicants.length}}</span>
                      <p></p>
                    </div>
                  </div>
                </b-card>
              </div>
              <button
                @click="jobInputModal"
                v-if="role === 'employer'"
                style="width: 100%; border-radius: 10px;"
                class="btn-outline-warning mb-2 mt-1"
              >
                Post Job
              </button>
            </b-card-body>
          </b-tab>
          <b-tab title="Applicants" style="max-height: 1000px; overflow-y: auto;">
            <b-card-body>
              <div v-for="job in user.jobs">
                <div v-for="user in job.confirmed_users">
                  <b-card class="text-left my-2">
                    <button href="#" style="float: right; margin-top: 8px !important; border: none;" class="mt-3 pt-2 ml-2 btn btn-outline-danger" @click="rejectConfirmedApplicant(job._id, user._id)"><i class="ti-close"></i></button>
                    <button href="#" style="float: right; margin-top: 8px !important; border: none;" class="mt-3 pt-2 btn btn-outline-info"><i class="ti-comment-alt"></i></button>
                    <h3 class="card-title user-hover mt-2" style="cursor: pointer;" @click="applicantDataModal(user)">{{user.name}}</h3>
                    <b>Job:</b><p>{{job.title}}</p>
                    <b>University: </b><p>{{user.company}}</p>
                    <b>LinkedIn: </b><p>{{user.social.linkedin}}</p>
                    <b>Skills: </b><p>{{user.skills.map(s => s.name).join(', ')}}</p>
                  </b-card>
                </div>
              </div>
            </b-card-body>
          </b-tab>
        </b-tabs>
      </b-card>
    </div>

    <HomePageUserModal :showModal="showApplicantData" @hideModal="hideHomePageUserModal" :user="applicantData" />
    <JobInputModal :showModal="showJobInputModal" @hideModal="hideJobInputModal" :user="user" @getData="getData"/>
    <ProfileInputModal :showModal="showEditProfileModal" :user="user" @hideModal="hideEditProfileInputModal"/>
    <JobInfoModal :showModal="showJobInfoModal" :job="jobInfoToBePassed" @hideModal="hideJobInfoModal"/>
    <DeleteConfirmModal :showModal="showDeleteConfirmModal" @hideModal="hideDeleteConfirmModal" @delete="deleteJobPosting" :job="jobInfoToBePassed"/>
    <EducationModal :show-modal="showEducationModal" @hideModal="hideEducationModal" :education="educationToBePassed" :buttonText="educationButtonText" :user="user"/>
    <ExperienceModal :show-modal="showExperienceModal" @hideModal="hideExperienceModal" :experience="experienceToBePassed" :button-text="experienceButtonText" :user="user" />
  </div>
</template>

<script>
import axios from 'axios'
import Fuse from 'fuse.js'
import gravatar from 'gravatar'

import NavBar from './NavBar'
import JobInputModal from './JobInputModal'
import ProfileInputModal from './ProfileEditModal'
import ProfileSettingsModal from './ProfileSettingsModal'
import JobInfoModal from './JobInfoModal'
import DeleteConfirmModal from './DeleteConfirmModal'
import EducationModal from './EducationModal'
import ExperienceModal from './ExperienceModal'
import HomePageUserModal from './HomePageUserModal'

import Gravatar from 'vue-gravatar'
import SkillSelect from './SkillSelect'

export default {
  name: 'Profile',
  components: {
    SkillSelect,
    NavBar,
    Gravatar,
    JobInputModal,
    ProfileInputModal,
    ProfileSettingsModal,
    JobInfoModal,
    DeleteConfirmModal,
    EducationModal,
    ExperienceModal,
    HomePageUserModal
  },
  data () {
    return {
      fuseOptions: {
        shouldSort: true,
        threshold: 0.4,
        location: 0,
        distance: 100,
        maxPatternLength: 32,
        minMatchCharLength: 1,
        keys: [
          'title'
        ]
      },
      showToast: false,
      toastContent: '',
      jobInfoToBePassed: {},
      educationToBePassed: {},
      experienceToBePassed: {},
      applicantData: {},
      educationButtonText: '',
      experienceButtonText: '',
      user_id: localStorage.getItem('user_id'),
      user: {},
      skillsToUpdate: [],
      role: localStorage.role,
      show: true,
      showJobInputModal: false,
      showEditProfileModal: false,
      showJobInfoModal: false,
      showDeleteConfirmModal: false,
      showEducationModal: false,
      showExperienceModal: false,
      showApplicantData: false,
      employerSearchJob: '',
      gravatarIcon: null
    }
  },
  computed: {
    employerJobs: {
      get: function (s) {
        if (!this.employerSearchJob) { return this.user.jobs } else {
          var fuse = new Fuse(this.user.jobs, this.fuseOptions)
          return fuse.search(this.employerSearchJob)
        }
      },
      set: function (newVal) {
        return newVal
      }
    }
  },
  watch: {
    user (newData) {
      this.user = newData
    }
  },
  methods: {
    logout () {
      localStorage.removeItem('jwtToken')
      this.$router.push({
        name: 'Login'
      })
    },
    showWindow (ref) {
      window.open(ref)
    },
    gravatarImage (email) {
      return gravatar.url(email)
    },
    formatDate (date) {
      if (date)
        return this.$moment(date).format('MMM Do YY')
      else return 'Present'
    },
    applicantDataModal (user) {
      this.applicantData = user
      this.showApplicantData = !this.showApplicantData
    },
    hideHomePageUserModal () {
      this.showApplicantData = false
    },
    jobInputModal () {
      this.showJobInputModal = !this.showJobInputModal
    },
    hideJobInputModal () {
      this.showJobInputModal = false
    },
    profileInputModal () {
      this.showEditProfileModal = !this.showEditProfileModal
    },
    hideEditProfileInputModal () {
      this.showEditProfileModal = false
    },
    jobInfoModal (job) {
      this.jobInfoToBePassed = job
      this.showJobInfoModal = !this.showJobInfoModal
    },
    hideJobInfoModal () {
      this.showJobInfoModal = false
    },
    deleteConfirmModal (job) {
      this.jobInfoToBePassed = job
      this.showDeleteConfirmModal = !this.showDeleteConfirmModal
    },
    hideDeleteConfirmModal () {
      this.showDeleteConfirmModal = false
    },
    editEducationModal (education) {
      this.educationToBePassed = education
      this.educationButtonText = 'Edit Education'
      this.showEducationModal = !this.showEducationModal
    },
    hideEducationModal () {
      this.showEducationModal = false
      this.getData()
    },
    editExperienceModal (experience) {
      this.experienceToBePassed = experience
      this.experienceButtonText = 'Edit Experience'
      this.showExperienceModal = !this.showExperienceModal
    },
    hideExperienceModal () {
      this.showExperienceModal = false
      this.getData()
    },
    addEducationModal () {
      this.educationToBePassed = {
        to: null,
        from: null,
        school: '',
        fieldofstudy: '',
        degree: '',
        current: false
      }
      this.educationButtonText = 'Add Education'
      this.showEducationModal = !this.showEducationModal
    },
    addExperienceModal () {
      this.experienceToBePassed = {
        to: null,
        from: null,
        company: '',
        title: '',
        location: '',
        description: '',
        current: true
      }
      this.experienceButtonText = 'Add Education'
      this.showExperienceModal = !this.showExperienceModal
    },
    deleteExperience (exp) {
      var headers = {
        Authorization: 'Bearer ' + localStorage.getItem('jwtToken').substring(4, localStorage.getItem('jwtToken').length)
      }

      axios.delete(`http://localhost:3000/api/profile/experience/${exp._id}`, {headers: headers})
        .then(response => {
          this.showToast = true
          this.toastContent = 'Deleted Successfully'
          this.getData()
        })
        .catch(err => {
          alert(err.response.data)
        })
    },
    deleteEducation (edu) {
      var headers = {
        Authorization: 'Bearer ' + localStorage.getItem('jwtToken').substring(4, localStorage.getItem('jwtToken').length)
      }
      axios.delete(`http://localhost:3000/api/profile/education/${edu._id}`, {headers: headers})
        .then(response => {
          this.showToast = true
          this.toastContent = 'Deleted Successfully'
          this.getData()
        })
        .catch(err => {
          alert(err.response.data)
        })
    },
    deleteJobPosting (id) {
      var headers = {
        Authorization: 'Bearer ' + localStorage.getItem('jwtToken').substring(4, localStorage.getItem('jwtToken').length)
      }
      axios.delete(`http://localhost:3000/api/jobs/${id}`, {headers: headers})
        .then(response => {
          this.showToast = true
          this.toastContent = 'Deleted Successfully'
          this.getData()
          this.hideDeleteConfirmModal()
          this.getData()
        })
        .catch(err => {
          alert(err.response.data)
        })
    },
    rejectConfirmedApplicant (job_id, user_id) {
      var headers = {
        Authorization: 'Bearer ' + localStorage.getItem('jwtToken').substring(4, localStorage.getItem('jwtToken').length)
      }

      axios.patch(`http://localhost:3000/api/jobs/rejectconfirmedapplicant`, {job: job_id, user: user_id}, {headers: headers})
        .then(response => {
          this.showToast = true
          this.toastContent = 'Rejected Successfully'
          this.getData()
          this.getData()
        })
        .catch(err => {
          alert('Delete could not happen.')
        })
    },
    addSkills (skills) {
      this.skillsToUpdate = skills
    },
    updateSkills () {
      var headers = {
        Authorization: 'Bearer ' + localStorage.getItem('jwtToken').substring(4, localStorage.getItem('jwtToken').length)
      }

      axios.post(`http://localhost:3000/api/profile/skills`, {data: this.skillsToUpdate, user: {id: this.user._id}}, {headers: headers})
        .then(response => {
          this.getData()
        })
        .catch(e => {
          console.log(e)
        })
    },
    getData () {
      var headers = {
        Authorization: 'Bearer ' + localStorage.getItem('jwtToken').substring(4, localStorage.getItem('jwtToken').length)
      }

      axios.get(`http://localhost:3000/api/user/${this.user_id}`, {headers: headers})
        .then(response => {
          this.user = response.data
        })
        .catch(e => {
          if (e.response.status === 401) {
            this.$router.push({
              name: 'Login'
            })
          }
        })
    }
  },
  created () {
    this.getData()
  }
}
</script>

